export const getTechCost = (techId: string): Partial<Record<string, number>> => {
  const config = TECHNOLOGIES[techId];
  if (!config) return {};
  return config.cost;
};

export const getTechByCategory = (category: TechCategory): TechConfig[] => {
  return Object.values(TECHNOLOGIES).filter((t) => t.category === category);
};

export const getTechByTier = (category: TechCategory, tier: number): TechConfig[] => {
  return Object.values(TECHNOLOGIES).filter((t) => t.category === category && t.tier === tier);
};
